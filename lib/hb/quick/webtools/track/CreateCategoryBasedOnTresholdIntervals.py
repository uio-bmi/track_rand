    @staticmethod    
    def execute(choices, galaxyFn=None, username=''):
        utfil = open(galaxyFn, 'w')
        genome = choices[0]
        trackName = choices[1].split(':')
        intervals = sorted([float(v.strip()) for v in choices[2].split(',')])
        geSource = ExternalTrackManager.getGESourceFromGalaxyOrVirtualTN(trackName, genome)
        
        for ge in geSource:
            value = ge.val
            counter=0
            while counter<len(intervals) and intervals[counter]<= value:
                counter+=1
            if counter == len(intervals):
                category = str(intervals[-1])+'-inf'
            else:
                category = str(intervals[counter-1])+'-'+str(intervals[counter]) if counter>0 else '0.0-'+str(intervals[0])
            print>>utfil, '%s\t%i\t%i\t%s' % (ge.chr, ge.start, ge.end, category)
            

